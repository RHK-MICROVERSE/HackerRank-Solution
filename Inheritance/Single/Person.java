
// THIS IS A PARENT CLASS named Person
public class Person {
    private int age;
    private String name;
    // Creating Setter
    public void setage(int ageYear){
        age = ageYear;
    }
    public void setname(String Fname) {
        name = Fname;

    }
    //Creating Getter
    public int getage() {
        return (age);
    }

    public String getname() {
        return (name);
    }

}
